public class Arith
{
	public static void main(String args[])
	{
		int r1=10;
		int r2=5;
		int sum=r1+r2;
		int diff=r1-r2;
		int mul=r1*r2;
		int div=r1/r2;
		System.out.println("sum="+sum);
		System.out.println("dif="+diff);
		System.out.println("mul="+mul);
		System.out.println("div="+div);
	}
}
